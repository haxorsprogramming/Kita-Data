% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

image = handles.image;
% image=double(image);
[tinggi, lebar] = size (image);
%GLCM = graycomatrix(image,'NumLevels',512,'Offset',[0 pixel_dist; -pixel_dist pixel_dist; -pixel_dist 0; -pixel_dist -pixel_dist]);
%stats = graycoprops(GLCM,{'Contrast','Correlation','Energy','Homogeneity'}); 
%GLCM = graycomatrix(image,'NumLevels',512,'offset',[0 1; -1 1; -1 0; -1 -1]);
%stats = graycoprops(GLCM,{'Contrast','Correlation','Energy','Homogeneity'});
%Contrast = stats.Contrast;
%Correlation = stats.Correlation;
%Energy = stats.Energy;
%Homogeneity = stats.Homogeneity;

GLCM0 = zeros(512,512);
total_piksel0 = 0;

GLCM45 = zeros(512,512);
total_piksel45 = 0;

GLCM90 = zeros(512,512);
total_piksel90 = 0;

GLCM135 = zeros(512,512);
total_piksel135 = 0;

% G0=GLCM0(image);
% G45=GLCM45(image);
% G90=GLCM90(image);
% G135=GLCM135(image);
% d = str2double(get(handles.edit4,'String'));

for y = 1 : tinggi
    for x = 1 : lebar-1
        %-- sudut 0
        a = image(y, x)+1;
        b = image(y, x+1)+1;
        GLCM0(a+1, b+1) = GLCM0(a+1, b+1)+1;
        total_piksel0 = total_piksel0+1;
    end
end

for y = 2 : tinggi
    for x = 1 : lebar-1
        %-- sudut 45
        a = image(y, x)+1;
        b = image(y-1, x+1)+1;
        GLCM45(a+1, b+1) = GLCM45(a+1, b+1)+1;
        total_piksel45 = total_piksel45+1;
    end
end

for y = 2 : tinggi
    for x = 1 : lebar
        %-- sudut 90
        a = image(y, x)+1;
        b = image(y-1, x)+1;
        GLCM90(a+1, b+1) = GLCM90(a+1, b+1)+1;
        total_piksel90 = total_piksel90+1;
    end
end

for y = 2 : tinggi
    for x = 2 : lebar        
        %-- sudut 135
        a = image(y, x)+1;
        b = image(y-1, x-1)+1;
        GLCM135(a+1, b+1) = GLCM135(a+1, b+1)+1;
        total_piksel135 = total_piksel135+1;
    end
end

GLCM0 = GLCM0 / total_piksel0;
GLCM45 = GLCM45 / total_piksel45;
GLCM90 = GLCM90 / total_piksel90;
GLCM135 = GLCM135 / total_piksel135;

%--hitungan ASM
Energy0 = 0.0;
Energy45 = 0.0;
Energy90 = 0.0;
Energy135 = 0.0;
for a=0 : 255
    for b=0 : 255
        Energy0 = Energy0+ (GLCM0(a+1, b+1) * GLCM0(a+1, b+1));
    Energy45 = Energy45+ (GLCM45(a+1, b+1) * GLCM45(a+1, b+1));
    Energy90 = Energy90+ (GLCM90(a+1, b+1) * GLCM90(a+1, b+1));
    Energy135 = Energy135+ (GLCM135(a+1, b+1) * GLCM135(a+1, b+1));
    end
end

%--hitugan Contrast
Contrast0 = 0.0;
Contrast45 = 0.0;
Contrast90 = 0.0;
Contrast135 = 0.0;
for a=0 : 255
    for b=0 : 255
